-- ----------------------------
-- Function structure for DELETE_CART
-- ----------------------------
CREATE OR REPLACE
PROCEDURE "FAKESHOP"."DELETE_CART" AS
BEGIN
    SELECT AMOUNT INTO CHANGE
    FROM CART
    WHERE PRODUCT_ID = PID AND CUSTOMER_ID = CID;

    DELETE FROM CART
    WHERE PRODUCT_ID = PID AND CUSTOMER_ID = CID;

    UPDATE_QUANTITY_CART(PID, CHANGE);
END;
/

