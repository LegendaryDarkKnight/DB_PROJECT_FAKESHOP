-- Function structure for GET_USER_ID
-- ----------------------------
CREATE OR REPLACE
FUNCTION "FAKESHOP"."GET_USER_ID" AS
BEGIN
	SELECT USER_ID INTO ID
	FROM ALL_USERS WHERE EMAIL_ID LIKE '%'||EMAIL||'%';
	RETURN ID;
END;
/

-- ----------------------------
-- Function structure for UPDATE_QUANTITY_CART
-- ----------------------------
CREATE OR REPLACE
PROCEDURE "FAKESHOP"."UPDATE_QUANTITY_CART" AS
BEGIN
    SELECT PRICE INTO PRICE1
    FROM PRODUCT
    WHERE PRODUCT_ID = PID;

    SELECT STOCK INTO MAX1
    FROM PRODUCT
    WHERE PRODUCT_ID = PID;

    DBMS_OUTPUT.PUT_LINE(MAX1);
    MAX1 := MAX1 - CHANGE;

		UPDATE PRODUCT
		SET STOCK = MAX1
		WHERE PRODUCT_ID = PID;

    UPDATE CART
    SET AMOUNT = MAX1, TOTAL_PRICE = MAX1*PRICE1
    WHERE PRODUCT_ID = PID AND
    AMOUNT > MAX1;
END;
/

